// HashSet Implementation
import java.util.*;
class HashSetExample {
    public static void main(String args[]) {
    
        HashSet hs = new HashSet();
        hs.add("D");
        hs.add("W");
        hs.add("G");
        hs.add("L");
        hs.add("Y");
        System.out.println("The elements available in the hash set are :" + hs);
    }
}